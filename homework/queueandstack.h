	#define MAX 128		

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
typedef struct stack{
	int a[MAX];
	int i;
}stack;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/*functions for integer stack*/
void push(stack *s, int num);
int pop(stack *s);
int empty(stack *s);
int full(stack *s);
void init(stack *s);
///////////////////////////////////////////////////////////////////////////////////////////
typedef struct queue{
	stack *s1;
	stack *s2;
}queue;
////////////////////////////////////////////////////////////////////////////////////////////////
	/*functions for queue*/

void enqueue(queue *q, int n);
int dequeue(queue *q);
int qempty(queue *q);
void qprint(queue *q);
void qinit(queue *q);
int qfull(queue *q);

